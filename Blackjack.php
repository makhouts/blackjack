<?php

class Blackjack {
    private $player;
    private $dealer;
    private $deck;

    public function getPlayer() {
        return $this->player;
        $this->player = new Blackjack();
    }

    public function getDealer() {
        return $this->dealer;
        $this->dealer = new Blackjack();
    }

    public function getDeck() {
        return $this->deck;
    }
};
